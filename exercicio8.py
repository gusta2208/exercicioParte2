def exercicioOito(num1, num2, num3):
    if num1 <= 0:
        return "Digite um número positivo!"
    else:
        if num2 <= 0:
            return "Digite um número positivo!"
        else:
            if num3 <= 0:
                return "Digite um número positivo!"
            else:
                return (num1 + num2 + num3) / 3

